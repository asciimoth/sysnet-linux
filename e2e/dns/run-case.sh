#!/usr/bin/env bash
set -euo pipefail

# Docker DNS e2e cases for `debug dns`.
#
# All cases use the same observable contract:
# 1. Arrange the container to look like one DNS-management setup.
# 2. Put the original upstream DNS server at a deterministic local address, or
#    omit original upstream configuration for *-no-upstream cases and pass that
#    address as a DNSProvider constructor fallback through the debug binary.
# 3. Start `debug dns`; it must detect the expected DnsMode and instantiate the
#    matching DNSProvider.
# 4. Run a normal system resolver query with dig. The query must go through the
#    DNS server installed by debug dns and appear in the debug log.
# 5. The query must still receive the upstream's deterministic answer, proving
#    the provider forwarded to the original upstream instead of looping back to
#    itself.
#
# Expected behavior per case:
# - direct: no recognized owner in /etc/resolv.conf, so DnsMode is "direct".
#   Direct writes /etc/resolv.conf with nameserver 127.0.0.1.
# - debian-resolvconf: /etc/resolv.conf is marked as resolvconf-owned and the
#   installed resolvconf implementation is Debian resolvconf, so DnsMode is
#   "debian-resolvconf". The sysnet-linux resolvconf record points at 127.0.0.1.
# - openresolv: /etc/resolv.conf is marked as resolvconf-owned and the installed
#   resolvconf implementation is openresolv, so DnsMode is "openresolv". The
#   sysnet-linux openresolv record points at 127.0.0.1.
# - systemd-resolved: a system bus and systemd-resolved are running,
#   /etc/resolv.conf points at the resolved stub 127.0.0.53, and resolved has
#   the mock upstream configured on eth0, so DnsMode is "systemd-resolved".
#   Resolved routes system queries to the debug TUN DNS.
#
# Docker bind-mounts /etc/resolv.conf into containers. The resolvconf packages
# normally maintain /etc/resolv.conf through a symlink to generated state, but
# they cannot replace Docker's mount. For resolvconf cases we still assert the
# real provider-owned resolvconf record, then copy the expected generated
# nameserver into Docker's mounted file before running dig.

case_name="${1:?usage: sysnet-dns-e2e <direct|direct-no-upstream|debian-resolvconf|debian-resolvconf-no-upstream|openresolv|openresolv-no-upstream|systemd-resolved|systemd-resolved-no-upstream>}"
expected_mode="${case_name%-no-upstream}"
no_upstream=0
if [[ "$case_name" == *-no-upstream ]]; then
	no_upstream=1
fi
query_name="sysnet-${case_name//[^a-zA-Z0-9]/-}-$$.e2e.test"
debug_log="/tmp/sysnet-debug.log"
upstream_log="/tmp/sysnet-upstream.log"
upstream_addr="127.0.0.54"

fail() {
	echo "FAIL[$case_name]: $*" >&2
	echo "--- debug log ---" >&2
	if [[ -f "$debug_log" ]]; then cat "$debug_log" >&2; fi
	echo "--- upstream log ---" >&2
	if [[ -f "$upstream_log" ]]; then cat "$upstream_log" >&2; fi
	echo "--- /etc/resolv.conf ---" >&2
	cat /etc/resolv.conf >&2 || true
	exit 1
}

wait_for_log() {
	local pattern="$1"
	local file="$2"
	local deadline=$((SECONDS + 20))
	while (( SECONDS < deadline )); do
		if [[ -f "$file" ]] && grep -Eq "$pattern" "$file"; then
			return 0
		fi
		sleep 0.2
	done
	return 1
}

cleanup_pids=()
cleanup() {
	local status=$?
	for pid in "${cleanup_pids[@]:-}"; do
		if kill -0 "$pid" 2>/dev/null; then
			kill "$pid" 2>/dev/null || true
			wait "$pid" 2>/dev/null || true
		fi
	done
	exit "$status"
}
trap cleanup EXIT

start_upstream() {
	SYSNET_DNS_E2E_UPSTREAM_ADDR="$upstream_addr" \
		python3 /usr/local/lib/sysnet-dns-e2e/upstream_dns.py >"$upstream_log" 2>&1 &
	cleanup_pids+=("$!")
	wait_for_log "upstream DNS listening" "$upstream_log" || fail "upstream DNS did not start"
}

write_direct_resolvconf() {
	cat > /etc/resolv.conf <<'EOF'
# static resolver for sysnet-linux docker e2e
nameserver 127.0.0.54
EOF
}

write_direct_resolvconf_without_upstreams() {
	cat > /etc/resolv.conf <<'EOF'
# static resolver for sysnet-linux docker e2e
# no original nameservers
EOF
}

write_resolvconf_owned_file() {
	cat > /etc/resolv.conf <<'EOF'
# resolv.conf(5) file generated by resolvconf
# DO NOT EDIT THIS FILE BY HAND -- YOUR CHANGES WILL BE OVERWRITTEN
nameserver 127.0.0.54
EOF
}

write_resolvconf_owned_file_without_upstreams() {
	cat > /etc/resolv.conf <<'EOF'
# resolv.conf(5) file generated by resolvconf
# DO NOT EDIT THIS FILE BY HAND -- YOUR CHANGES WILL BE OVERWRITTEN
# no original nameservers
EOF
}

start_systemd_resolved() {
	local configure_upstream="${1:-1}"
	mkdir -p /run/dbus /etc/systemd
	local upstream_octets
	local upstream_ifidx
	dbus-daemon --system --fork
	cat > /etc/systemd/resolved.conf <<'EOF'
[Resolve]
DNSStubListener=yes
FallbackDNS=
EOF
	/lib/systemd/systemd-resolved &
	cleanup_pids+=("$!")
	local deadline=$((SECONDS + 20))
	while (( SECONDS < deadline )); do
		if busctl --system call org.freedesktop.resolve1 /org/freedesktop/resolve1 org.freedesktop.DBus.Peer Ping >/dev/null 2>&1; then
			break
		fi
		sleep 0.2
	done
	busctl --system call org.freedesktop.resolve1 /org/freedesktop/resolve1 org.freedesktop.DBus.Peer Ping >/dev/null 2>&1 || fail "systemd-resolved did not register on D-Bus"
	if [[ "$configure_upstream" == 1 ]]; then
		upstream_ifidx="$(cat /sys/class/net/eth0/ifindex)"
		upstream_octets="${upstream_addr//./ }"
		busctl --system call org.freedesktop.resolve1 /org/freedesktop/resolve1 org.freedesktop.resolve1.Manager SetLinkDNS "ia(iay)" "$upstream_ifidx" 1 2 4 $upstream_octets || fail "failed to configure resolved upstream DNS"
		busctl --system call org.freedesktop.resolve1 /org/freedesktop/resolve1 org.freedesktop.resolve1.Manager SetLinkDefaultRoute "ib" "$upstream_ifidx" true || fail "failed to configure resolved default route"
		busctl --system call org.freedesktop.resolve1 /org/freedesktop/resolve1 org.freedesktop.resolve1.Manager SetLinkDomains "ia(sb)" "$upstream_ifidx" 1 "." true || fail "failed to configure resolved route-only root domain"
	fi
	cat > /etc/resolv.conf <<'EOF'
# This is /run/systemd/resolve/stub-resolv.conf managed by man:systemd-resolved(8).
nameserver 127.0.0.53
EOF
}

run_debug() {
	local fallback_env=()
	if [[ "$no_upstream" == 1 ]]; then
		fallback_env=("SYSNET_DEBUG_DNS_FALLBACKS=${upstream_addr}:53")
	fi
	env "${fallback_env[@]}" /usr/local/bin/sysnet-debug dns >"$debug_log" 2>&1 &
	cleanup_pids+=("$!")
	wait_for_log "^${expected_mode} <nil>$" "$debug_log" || fail "debug dns did not report DnsMode $expected_mode"
	wait_for_log "proxying DNS on .*:53 until Ctrl\\+C" "$debug_log" || fail "debug dns did not start proxying"
}

assert_query() {
	local out
	if ! out="$(dig +time=2 +tries=1 +short "$query_name" A 2>&1)"; then
		fail "dig failed: $out"
	fi
	if ! grep -q "203.0.113.10" <<<"$out"; then
		fail "dig returned unexpected answer: $out"
	fi
	wait_for_log "dns request .*${query_name}\\./1/1" "$debug_log" || fail "debug dns did not log intercepted query $query_name"
}

mirror_resolvconf_record_to_docker_resolvconf() {
	local record="$1"
	local record_contents
	record_contents="$(resolvconf -l "$record" 2>/dev/null || resolvconf -l 2>/dev/null)"
	if ! grep -q '^nameserver 127\.0\.0\.1$' <<<"$record_contents"; then
		fail "resolvconf record $record does not point at 127.0.0.1"
	fi
	cat > /etc/resolv.conf <<'EOF'
# resolv.conf(5) file generated by resolvconf for sysnet-linux Docker e2e
# Docker keeps this path mounted, so the test mirrors the generated record.
nameserver 127.0.0.1
EOF
}

case "$case_name" in
	direct|direct-no-upstream)
		start_upstream
		if [[ "$no_upstream" == 1 ]]; then
			write_direct_resolvconf_without_upstreams
		else
			write_direct_resolvconf
		fi
		run_debug
		grep -q '^nameserver 127\.0\.0\.1$' /etc/resolv.conf || fail "Direct did not install 127.0.0.1 in /etc/resolv.conf"
		assert_query
		;;
	debian-resolvconf|debian-resolvconf-no-upstream)
		start_upstream
		if [[ "$no_upstream" == 1 ]]; then
			write_resolvconf_owned_file_without_upstreams
		else
			write_resolvconf_owned_file
		fi
		resolvconf --version >/tmp/resolvconf-version.log 2>&1 || true
		grep -q '^Debian resolvconf' /tmp/resolvconf-version.log || fail "resolvconf --version did not identify Debian resolvconf"
		run_debug
		mirror_resolvconf_record_to_docker_resolvconf sysnet-linux
		assert_query
		;;
	openresolv|openresolv-no-upstream)
		start_upstream
		if [[ "$no_upstream" == 1 ]]; then
			write_resolvconf_owned_file_without_upstreams
		else
			write_resolvconf_owned_file
		fi
		resolvconf --version >/tmp/resolvconf-version.log 2>&1 || fail "expected openresolv --version to succeed"
		run_debug
		mirror_resolvconf_record_to_docker_resolvconf sysnet-linux
		assert_query
		;;
	systemd-resolved|systemd-resolved-no-upstream)
		upstream_addr="$(ip -4 -o addr show dev eth0 | awk '{split($4, a, "/"); print a[1]; exit}')"
		start_upstream
		if [[ "$no_upstream" == 1 ]]; then
			start_systemd_resolved 0
		else
			start_systemd_resolved 1
		fi
		run_debug
		assert_query
		;;
	*)
		fail "unknown case $case_name"
		;;
esac

echo "PASS[$case_name]: mode=$expected_mode query=$query_name"
